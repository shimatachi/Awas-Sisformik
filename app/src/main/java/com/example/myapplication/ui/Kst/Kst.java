package com.example.myapplication.ui.Kst;

public class Kst {
    private String matkul;
    private String sks;

    public Kst(String matkul, String sks) {
        this.matkul = matkul;
        this.sks = sks;
    }

    public String getMatkul() {
        return matkul;
    }

    public String getSks() {
        return sks;
    }
}
